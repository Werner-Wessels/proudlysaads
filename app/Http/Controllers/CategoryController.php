        $listings = Listing::all()
            ->where('category_id', '=', $id)
            ->where('is_active', '=', true)
            ->where('is_approved','=',true );

        return view('categories.show', [
            'area' => $area,
            'listings' => $listings,
            'categories' => Category::all(),
            'locations' => Location::all(),

        ]);
    }

